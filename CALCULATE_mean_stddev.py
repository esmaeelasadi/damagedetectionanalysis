import cv2
# ---------------------------------------------------------------------------------------



# CALCULATE MEAN AND STANDARD DEVIATION OF DATA -----------------------------------------
# ---------------------------------------------------------------------------------------
def CALCULATE_mean_stddev(data):
	
	value = cv2.meanStdDev(data)

	return value[0][0][0], value[1][0][0]
